package com.hopetoknow.from_2500_to_2999._2807_insert_greatest_common_divisors_in_linked_list;

public class ListNode {
    int val;
    ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
